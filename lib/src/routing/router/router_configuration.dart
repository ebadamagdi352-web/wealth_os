/// Builds the application's [GoRouter] from [RouteRegistry].
///
/// This is an **adapter**, and the direction of the dependency is the point:
/// this file reads the route table, and the route table has never heard of
/// GoRouter. Replacing GoRouter means rewriting this file and nothing else.
///
/// There is no path literal anywhere in here. Every [GoRoute] is generated by
/// iterating [RouteRegistry.allRoutes], so a route cannot exist in the router
/// without existing in the registry — there is only one list, so the two cannot
/// disagree.
abstract final class RouterConfiguration {
  /// Creates the router. Called once, by `AppRouter`.
  static GoRouter create() {
    // Checked before a single route is built. GoRouter accepts duplicate paths
    // silently, with the second declaration shadowing the first — a bug that
    // surfaces as one screen inexplicably rendering another's content, with no
    // error reported anywhere. Validating first turns that into a StateError
    // that names the offending path, before the first frame.
    RouteRegistry.validate();

    return GoRouter(
      initialLocation: RoutePaths.home,
      debugLogDiagnostics: AppEnvironmentConfig.current.isPreProduction,
      routes: <RouteBase>[
        for (final AppRoute route in RouteRegistry.allRoutes)
          GoRoute(
            name: route.nameValue,
            path: route.path,
            builder: (BuildContext context, GoRouterState state) =>
                _screenFor(route),
          ),
      ],
    );
  }

  /// Maps a route to the widget that renders it.
  ///
  /// Currently a two-way split: the root shows [AppPlaceholderScreen], every
  /// other destination shows [ComingSoonPage]. When real screens exist this
  /// becomes an exhaustive `switch` over [RouteName], at which point adding a
  /// route without giving it a screen will not compile — which is the reason
  /// [RouteName] was made an enum in Task 009.
  static Widget _screenFor(AppRoute route) {
    if (route.name == RouteName.home) {
      return const AppPlaceholderScreen();
    }
    return ComingSoonPage(route: route);
  }
}

/// The application's root surface, shown at `/`.
///
/// This is the widget that existed in `app.dart` before the router arrived. It
/// has moved rather than disappeared: with `MaterialApp.router`, no screen can
/// be hardcoded into the app root, because what is displayed is decided by the
/// route table. The root is now a *route*, and this is its screen.
///
/// It remains importable from `package:wealth_os/src/app/app.dart`, which
/// re-exports it, so existing imports continue to resolve.
///
/// The title is [AppConstants.appName] rather than a string literal. That
/// constant is the brand name, and a brand name is the one piece of text in the
/// product that is deliberately *not* translated — `appName` is `"Wealth OS"` in
/// both `app_en.arb` and `app_ar.arb` for exactly this reason.
class AppPlaceholderScreen extends StatelessWidget {
  const AppPlaceholderScreen({super.key});

  @override
  Widget build(BuildContext context) {
    return const Scaffold(
      body: Center(
        child: Text(AppConstants.appName),
      ),
    );
  }
}

/// The temporary surface shown for every route other than the root.
///
/// Contains a title and the words "coming soon". Nothing else — no business
/// logic, no navigation affordance, no state.
///
/// It takes an [AppRoute] rather than a `String`, so it cannot be constructed
/// for a destination that does not exist in the registry. The placeholder is
/// temporary; the discipline is not.
class ComingSoonPage extends StatelessWidget {
  const ComingSoonPage({required this.route, super.key});

  /// The route this page is standing in for.
  final AppRoute route;

  /// A human-readable title, derived from the route's identifier.
  ///
  /// **Not localized, and that is a known defect with a scheduled fix.**
  ///
  /// [AppRoute.titleKey] holds ARB keys — `navDashboard`, `navAccounts`, and six
  /// more — that do not exist in `app_en.arb` or `app_ar.arb` yet, and the
  /// localization layer is out of scope for this task. `gen-l10n` emits a typed
  /// getter per *declared* key rather than a lookup-by-string map, so a key that
  /// was never declared cannot be resolved at all.
  ///
  /// Deriving the title from the enum was chosen over inventing an English
  /// literal because the wrongness stays *visible*: `"Dashboard"` appearing in an
  /// Arabic build is obviously a defect, whereas a hardcoded `'Dashboard'` would
  /// look finished and quietly fail translation review.
  String get _title {
    final String identifier = route.name.name;
    return identifier[0].toUpperCase() + identifier.substring(1);
  }

  @override
  Widget build(BuildContext context) {
    final AppLocalizations l10n = AppLocalizations.of(context);

    return Scaffold(
      appBar: AppBar(
        title: Text(_title),
      ),
      body: Center(
        child: Text(l10n.comingSoon),
      ),
    );
  }
}
