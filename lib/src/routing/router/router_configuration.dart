/// Builds the application's [GoRouter] from [RouteRegistry].
///
/// An adapter. It reads the route table; the route table has never heard of GoRouter.
abstract final class RouterConfiguration {
  /// Creates the router. Called once, by `AppRouter`.
  static GoRouter create() {
    // GoRouter accepts duplicate paths silently, with the second declaration shadowing
    // the first — a bug that surfaces as one screen inexplicably rendering another's
    // content, with no error reported anywhere.
    RouteRegistry.validate();

    return GoRouter(
      initialLocation: RoutePaths.home,
      debugLogDiagnostics: AppEnvironmentConfig.current.isPreProduction,
      routes: <RouteBase>[
        // `/` redirects into the shell. It cannot simply *render* a screen: `/` lives
        // outside StatefulShellRoute, so a widget built here would appear with no
        // navigation bar and the user would have no way to reach anything else.
        GoRoute(
          path: RoutePaths.home,
          name: RouteName.home.name,
          redirect: (BuildContext context, GoRouterState state) =>
              RoutePaths.dashboard,
        ),

        // =================================================================
        // ⚠️ REGISTRY VIOLATION — quarantined, and it must not stay
        // =================================================================
        //
        // Since Task 009, the rule has been absolute: **no route is defined outside
        // RouteRegistry.** Every GoRoute in this file is generated by iterating the
        // registry, which is what makes it impossible for the router and the route table
        // to disagree.
        //
        // This route breaks that rule. It is declared here, with a **path literal** — the
        // other thing this codebase has never had outside `route_paths.dart`.
        //
        // Why: the add-transaction screen needs a route, and a route needs three lines in
        // `routing\` —
        //
        //   route_names.dart    : addTransaction,
        //   route_paths.dart    : static const String addTransaction = '/add-transaction';
        //   route_registry.dart : AppRoute(name: ..., path: ..., titleKey: ...,
        //                                  iconKey: 'add', showInNavigation: false),
        //
        // — and this task permits modifying exactly one file, which is this one.
        //
        // What it costs, concretely:
        //
        //   * `RouteRegistry.validate()` cannot see this path. If a future registry entry
        //     also claims '/add-transaction', GoRouter will silently shadow one with the
        //     other and the validator will report nothing wrong.
        //   * `RouteRegistry.findByPath('/add-transaction')` returns null, so a future
        //     auth guard reading `requiresAuthentication` would treat this route as
        //     unprotected — a form that writes to a user's ledger.
        //   * The path exists in two places the moment anyone adds it to `route_paths.dart`.
        //
        // **Authorize the three lines and this block is deleted**, replaced by the same
        // `if (!route.showInNavigation)` loop below that already handles every other
        // off-shell route. See TASK_016_REPORT.md.
        GoRoute(
          path: '/add-transaction',
          name: 'addTransaction',
          builder: (BuildContext context, GoRouterState state) =>
              _themed(context, const AddTransactionPage()),
        ),
        // =================================================================

        // Any other route outside the shell. `home` is excluded because it is declared
        // above with a redirect rather than a builder.
        for (final AppRoute route in RouteRegistry.allRoutes)
          if (!route.showInNavigation && route.name != RouteName.home)
            GoRoute(
              name: route.nameValue,
              path: route.path,
              builder: (BuildContext context, GoRouterState state) =>
                  _themed(context, _screenFor(route)),
            ),

        // Routes inside the shell.
        //
        // `indexedStack` gives each branch its own Navigator and keeps all of them alive —
        // which preserves a tab's scroll position and its half-filled form when the user
        // switches away and back.
        StatefulShellRoute.indexedStack(
          builder: (
            BuildContext context,
            GoRouterState state,
            StatefulNavigationShell navigationShell,
          ) {
            // GoRouter's type is unwrapped here, not inside NavigationShell. The shell
            // takes a widget, an index and a callback — three plain values — so it can be
            // pumped in a test with no router at all.
            return _themed(
              context,
              NavigationShell(
                currentIndex: navigationShell.currentIndex,
                onDestinationSelected: (int index) {
                  navigationShell.goBranch(
                    index,
                    // Tapping the branch you are already on returns it to its root rather
                    // than doing nothing.
                    initialLocation: index == navigationShell.currentIndex,
                  );
                },
                child: navigationShell,
              ),
            );
          },
          branches: <StatefulShellBranch>[
            for (final AppRoute route in RouteRegistry.navigationRoutes)
              StatefulShellBranch(
                routes: <RouteBase>[
                  GoRoute(
                    name: route.nameValue,
                    path: route.path,
                    builder: (BuildContext context, GoRouterState state) =>
                        _screenFor(route),
                  ),
                ],
              ),
          ],
        ),
      ],
    );
  }

  /// Maps a registry route to the widget that renders it.
  ///
  /// Exhaustive over [RouteName]. Adding a route without giving it a screen will not
  /// compile — which is why `RouteName` was made an enum in Task 009.
  ///
  /// Note that the add-transaction page is **absent** from this switch. It has no
  /// `RouteName`, because it is not in the registry. That absence is the violation
  /// described above, made visible.
  static Widget _screenFor(AppRoute route) {
    return switch (route.name) {
      RouteName.home => const AppPlaceholderScreen(),
      RouteName.dashboard => const DashboardPage(),
      RouteName.accounts => const AccountsPage(),
      RouteName.transactions => const TransactionsPage(),
      RouteName.assets ||
      RouteName.goals ||
      RouteName.reports ||
      RouteName.settings =>
        ComingSoonPage(route: route),
    };
  }

  // ---------------------------------------------------------------------
  // ⚠️ TEMPORARY THEME BRIDGE — delete when app.dart wires the theme
  // ---------------------------------------------------------------------

  static final ThemeData _lightTheme = AppTheme.light();
  static final ThemeData _darkTheme = AppTheme.dark();

  /// Applies the design system to a subtree.
  ///
  /// **This does not belong here.** The theme belongs on `MaterialApp`:
  ///
  /// ```dart
  /// MaterialApp.router(
  ///   theme: AppTheme.light(),
  ///   darkTheme: AppTheme.dark(),
  ///   themeMode: ThemeMode.system,
  /// )
  /// ```
  ///
  /// Three lines in `app.dart`, which has not been on a modify list since Task 010.
  ///
  /// Two costs, both real:
  ///
  /// * It reads `platformBrightness` directly, so it cannot honour a user's explicit
  ///   light/dark choice. `settingsProvider.themeMode` exists and is unreachable, because
  ///   `bootstrap.dart` still has no `ProviderScope`.
  /// * Anything rendered *above* the router — a dialog, an overlay, the date picker's
  ///   own surfaces — falls outside this `Theme`.
  static Widget _themed(BuildContext context, Widget child) {
    final Brightness brightness = MediaQuery.platformBrightnessOf(context);
    return Theme(
      data: brightness == Brightness.dark ? _darkTheme : _lightTheme,
      child: child,
    );
  }
}

/// The former root surface.
///
/// **No longer reachable.** `/` redirects to `/dashboard`, so nothing builds it. Retained
/// because `app.dart` re-exports it and `test\widget_test.dart` imports it.
class AppPlaceholderScreen extends StatelessWidget {
  const AppPlaceholderScreen({super.key});

  @override
  Widget build(BuildContext context) {
    return const Scaffold(
      body: Center(
        child: Text(AppConstants.appName),
      ),
    );
  }
}

/// The temporary surface shown for destinations that have no screen yet.
class ComingSoonPage extends StatelessWidget {
  const ComingSoonPage({required this.route, super.key});

  /// The route this page is standing in for.
  final AppRoute route;

  @override
  Widget build(BuildContext context) {
    final AppLocalizations l10n = AppLocalizations.of(context);

    return Scaffold(
      appBar: AppBar(
        // Shares one derivation with the navigation bar's labels, so a route's title
        // cannot read one way in the app bar and another below it.
        title: Text(temporaryRouteTitle(route.name)),
      ),
      body: Center(
        child: Text(l10n.comingSoon),
      ),
    );
  }
}
